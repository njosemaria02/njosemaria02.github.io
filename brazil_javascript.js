function bigzoom() {
  var yourImg = document.getElementById('yourImgId');
  if(yourImg && yourImg.style) {
    yourImg.style.height = '408px';
    yourImg.style.width = '652px';
  }
}

function backzoom() {
  var yourImg = document.getElementById('yourImgId');
  if(yourImg && yourImg.style) {
    yourImg.style.height = '272px';
    yourImg.style.width = '435px';
  }
}
